import csv

from rest_framework import serializers

from .models import KnowledgeBase, KnowledgeItem, LLMConfig, AutoGeneratedTitle


class KnowledgeBaseSerializer(serializers.ModelSerializer):
    MANDATORY_COLUMNS = ["intent", "answer", "url"]

    class Meta:
        model = KnowledgeBase
        fields = "__all__"

    # extra step when validating: the file must contain the following columns: intent, answer, url
    def validate(self, data):
        if "original_file" in data:
            f = data["original_file"]
            decoded_file = f.read().decode("utf-8").splitlines()
            reader = csv.DictReader(decoded_file)
            if not all(elem in reader.fieldnames for elem in self.MANDATORY_COLUMNS):
                raise serializers.ValidationError(
                    "The file must contain the following columns: "
                    + ", ".join(self.MANDATORY_COLUMNS)
                )
            f.seek(0)
        return data


class KnowledgeBaseFromUrlSerializer(KnowledgeBaseSerializer):
    url = serializers.URLField()

    class Meta:
        model = KnowledgeBase
        fields = ["name", "lang", "url"]


class KnowledgeItemSerializer(serializers.ModelSerializer):
    class Meta:
        model = KnowledgeItem
        fields = "__all__"


class AutoGeneratedTitleSerializer(serializers.ModelSerializer):
    class Meta:
        model = AutoGeneratedTitle
        fields = "__all__"


class LLMConfigSerializer(serializers.ModelSerializer):
    status = serializers.CharField(read_only=True)

    class Meta:
        model = LLMConfig
        fields = "__all__"
